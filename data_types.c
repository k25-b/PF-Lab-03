#include <stdio.h>

int main() {
    int age = 20;               
    float height = 5.9;         
    double pi = 3.1415926535;   
    char grade = 'A';           


    printf("Integer (Age): %d\n", age);
    printf("Float (Height): %f\n", height);
    printf("Double (Pi): %lf\n", pi);
    printf("Character (Grade): %c\n", grade);

    return 0;
}
